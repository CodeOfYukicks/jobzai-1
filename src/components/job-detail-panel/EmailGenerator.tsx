import { useState, useEffect, useCallback } from 'react';
import { motion, AnimatePresence } from 'framer-motion';
import {
  Sparkles,
  Copy,
  Check,
  Loader2,
  Save,
  Mail,
  Maximize2,
  HelpCircle,
  MessageSquare,
  FileText,
  Send,
} from 'lucide-react';
import { toast } from '@/contexts/ToastContext';
import { JobApplication, GeneratedEmail } from '../../types/job';
import { useUserProfile } from '../../hooks/useUserProfile';
import { 
  generateCoverLetter, 
  generateFollowUp, 
  generateInterviewPrepQuestions, 
  generateQuestionsToAsk, 
  generateThankYouEmail 
} from '../../lib/aiEmailGenerator';
import { FocusModeEditor } from './FocusModeEditor';

type DocumentType = 'cover_letter' | 'follow_up' | 'interview_prep' | 'questions_to_ask' | 'thank_you';

interface EmailGeneratorProps {
  job: JobApplication;
  type: DocumentType;
  onSave?: (email: GeneratedEmail) => Promise<void>;
  autoGenerate?: boolean;
  onGenerationComplete?: () => void;
  onContentGenerated?: (content: string) => void;
  hideUI?: boolean;
}

export const EmailGenerator = ({ job, type, onSave, autoGenerate = false, onGenerationComplete, onContentGenerated, hideUI = false }: EmailGeneratorProps) => {
  const { profile, loading: profileLoading } = useUserProfile();
  const [generatedContent, setGeneratedContent] = useState('');
  const [isGenerating, setIsGenerating] = useState(false);
  const [isCopied, setIsCopied] = useState(false);
  const [isSaving, setIsSaving] = useState(false);
  const [editMode, setEditMode] = useState(false);
  const [editedContent, setEditedContent] = useState('');
  const [showFocusMode, setShowFocusMode] = useState(false);
  const [hasAutoGenerated, setHasAutoGenerated] = useState(false);

  const getDocumentInfo = (docType: DocumentType) => {
    switch (docType) {
      case 'cover_letter':
        return { title: 'Cover Letter', icon: Mail };
      case 'follow_up':
        return { title: 'Follow-Up Email', icon: Send };
      case 'interview_prep':
        return { title: 'Interview Prep Questions', icon: HelpCircle };
      case 'questions_to_ask':
        return { title: 'Questions to Ask', icon: MessageSquare };
      case 'thank_you':
        return { title: 'Thank You Email', icon: FileText };
      default:
        return { title: 'Document', icon: Sparkles };
    }
  };

  const { title, icon: Icon } = getDocumentInfo(type);

  const handleGenerate = useCallback(async () => {
    if (!profile) {
      toast.error('Please complete your profile first to generate personalized documents');
      return;
    }

    setIsGenerating(true);
    setEditMode(false);
    
    try {
      let result;
      switch (type) {
        case 'cover_letter':
          result = await generateCoverLetter(job, profile);
          break;
        case 'follow_up':
          result = await generateFollowUp(job, profile);
          break;
        case 'interview_prep':
          result = await generateInterviewPrepQuestions(job, profile);
          break;
        case 'questions_to_ask':
          result = await generateQuestionsToAsk(job, profile);
          break;
        case 'thank_you':
          result = await generateThankYouEmail(job, profile);
          break;
        default:
          throw new Error(`Unknown document type: ${type}`);
      }

      if (result.error) {
        toast.error(result.errorMessage || 'Failed to generate document');
        return;
      }

      setGeneratedContent(result.content);
      setEditedContent(result.content);
      onContentGenerated?.(result.content);
      toast.success(`${title} generated successfully!`);
      onGenerationComplete?.();
    } catch (error) {
      console.error('Error generating document:', error);
      toast.error('An error occurred while generating the document');
      onGenerationComplete?.();
    } finally {
      setIsGenerating(false);
    }
  }, [job, profile, type, title, onGenerationComplete, onContentGenerated]);

  // Auto-generate on mount if autoGenerate is true (runs even when hideUI is true)
  useEffect(() => {
    if (autoGenerate && !hasAutoGenerated && profile && !profileLoading) {
      setHasAutoGenerated(true);
      handleGenerate();
    }
  }, [autoGenerate, hasAutoGenerated, profile, profileLoading, handleGenerate]);

  const handleCopy = async () => {
    const contentToCopy = editMode ? editedContent : generatedContent;
    
    try {
      await navigator.clipboard.writeText(contentToCopy);
      setIsCopied(true);
      toast.success('Copied to clipboard!');
      setTimeout(() => setIsCopied(false), 2000);
    } catch (error) {
      toast.error('Failed to copy to clipboard');
    }
  };

  const handleSave = async () => {
    if (!onSave) return;
    
    const contentToSave = editMode ? editedContent : generatedContent;
    
    if (!contentToSave) {
      toast.error('No content to save');
      return;
    }

    setIsSaving(true);
    
    try {
      const newEmail: GeneratedEmail = {
        id: crypto.randomUUID(),
        type,
        content: contentToSave,
        createdAt: new Date().toISOString(),
      };

      await onSave(newEmail);
      toast.success('Email saved to history!');
      setEditMode(false);
    } catch (error) {
      console.error('Error saving email:', error);
      toast.error('Failed to save email');
    } finally {
      setIsSaving(false);
    }
  };

  const handleSaveFromFocusMode = async (content: string) => {
    setEditedContent(content);
    setGeneratedContent(content);
    setEditMode(false);
    
    if (onSave) {
      const newEmail: GeneratedEmail = {
        id: crypto.randomUUID(),
        type,
        content,
        createdAt: new Date().toISOString(),
      };
      await onSave(newEmail);
    }
  };

  // If hideUI is true, don't render UI - just handle generation in background
  if (hideUI) {
    return null;
  }

  return (
    <>
      <div className="space-y-6">
        {/* Minimal Header */}
      <div className="flex items-center justify-between">
        <div className="flex items-center gap-3">
            <div className="p-3 rounded-xl bg-gradient-to-br from-purple-500 to-indigo-500 shadow-sm">
              <Icon className="w-6 h-6 text-white" />
          </div>
            <h3 className="text-xl font-bold text-gray-900 dark:text-white">
              {title}
            </h3>
        </div>

        <button
          onClick={handleGenerate}
          disabled={isGenerating || profileLoading}
            className="inline-flex items-center gap-2 px-6 py-3 rounded-xl bg-gradient-to-r from-purple-600 to-indigo-600 text-white font-semibold hover:from-purple-700 hover:to-indigo-700 shadow-md hover:shadow-lg transition-all disabled:opacity-50 disabled:cursor-not-allowed"
        >
          {isGenerating ? (
            <>
                <Loader2 className="w-5 h-5 animate-spin" />
              <span>Generating...</span>
            </>
          ) : (
            <>
                <Sparkles className="w-5 h-5" />
              <span>Generate</span>
            </>
          )}
        </button>
      </div>

      {/* Generated Content Area */}
      <AnimatePresence mode="wait">
        {generatedContent ? (
          <motion.div
            initial={{ opacity: 0, y: 10 }}
            animate={{ opacity: 1, y: 0 }}
            exit={{ opacity: 0, y: -10 }}
              className="space-y-4"
          >
            {/* Content Display/Edit */}
            <div className="relative">
              {editMode ? (
                <textarea
                  value={editedContent}
                  onChange={(e) => setEditedContent(e.target.value)}
                    className="w-full min-h-[400px] p-6 rounded-xl border border-gray-200 dark:border-[#3d3c3e] bg-white dark:bg-[#2b2a2c] text-gray-900 dark:text-gray-100 focus:border-purple-500 dark:focus:border-purple-400 focus:ring-2 focus:ring-purple-500/20 outline-none transition-all resize-none text-base leading-relaxed shadow-sm"
                />
              ) : (
                  <div className="p-6 rounded-xl bg-white dark:bg-[#2b2a2c] border border-gray-200 dark:border-[#3d3c3e] shadow-sm">
                    <pre className="whitespace-pre-wrap text-base text-gray-900 dark:text-gray-100 font-sans leading-relaxed">
                    {generatedContent}
                  </pre>
                </div>
              )}
            </div>

            {/* Action Buttons */}
              <div className="flex items-center gap-3 flex-wrap">
                <button
                  onClick={() => setShowFocusMode(true)}
                  className="inline-flex items-center gap-2 px-5 py-2.5 rounded-xl bg-gradient-to-r from-purple-600 to-indigo-600 text-white font-medium hover:from-purple-700 hover:to-indigo-700 shadow-sm hover:shadow-md transition-all"
                >
                  <Maximize2 className="w-4 h-4" />
                  <span>Open in Focus Mode</span>
                </button>

              <button
                onClick={handleCopy}
                  className="inline-flex items-center gap-2 px-4 py-2.5 rounded-xl bg-white dark:bg-[#2b2a2c] border border-gray-200 dark:border-[#3d3c3e] text-gray-700 dark:text-gray-300 hover:bg-gray-50 dark:hover:bg-[#3d3c3e] shadow-sm hover:shadow transition-colors"
              >
                {isCopied ? (
                  <>
                    <Check className="w-4 h-4 text-green-600" />
                    <span>Copied!</span>
                  </>
                ) : (
                  <>
                    <Copy className="w-4 h-4" />
                    <span>Copy</span>
                  </>
                )}
              </button>

              {onSave && (
                <button
                  onClick={handleSave}
                  disabled={isSaving || !generatedContent}
                    className="inline-flex items-center gap-2 px-4 py-2.5 rounded-xl bg-white dark:bg-[#2b2a2c] border border-gray-200 dark:border-[#3d3c3e] text-gray-700 dark:text-gray-300 hover:bg-gray-50 dark:hover:bg-[#3d3c3e] shadow-sm hover:shadow transition-colors disabled:opacity-50 disabled:cursor-not-allowed"
                >
                  {isSaving ? (
                    <>
                      <Loader2 className="w-4 h-4 animate-spin" />
                      <span>Saving...</span>
                    </>
                  ) : (
                    <>
                      <Save className="w-4 h-4" />
                      <span>Save to History</span>
                    </>
                  )}
                </button>
              )}
            </div>
          </motion.div>
        ) : (
            <div className="p-16 rounded-xl bg-gradient-to-br from-gray-50 to-gray-100 dark:from-gray-800 dark:to-gray-900 border border-gray-200 dark:border-[#3d3c3e] text-center">
              <div className="inline-flex p-4 rounded-full bg-gradient-to-br from-purple-500 to-indigo-500 mb-4">
                <Sparkles className="w-8 h-8 text-white" />
              </div>
              <p className="text-lg font-medium text-gray-700 dark:text-gray-300">
                Ready to generate your {title.toLowerCase()}
            </p>
          </div>
        )}
      </AnimatePresence>
                    </div>

      {/* Focus Mode Editor */}
      <FocusModeEditor
        isOpen={showFocusMode}
        onClose={() => setShowFocusMode(false)}
        initialContent={editMode ? editedContent : generatedContent}
        documentType={type}
        title={title}
        onSave={handleSaveFromFocusMode}
      />
    </>
  );
};

